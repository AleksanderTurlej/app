    {
        $builder = $this->createQueryBuilder('u');


//dd($userName);
        if ($userName) {

            $builder->andWhere('u.username = :value')
                ->setParameter('value', $userName, \PDO::PARAM_STR);
        }

        return $builder
            ->getQuery()
            ->getResult()
            ;

    }


